function businessField(key: string): FieldDefinition {
  return {
    key,
    label: fieldLabels[key] ?? key,
    technical: false,
    visibleInTable: true,
  };
}

function technicalField(key: string): FieldDefinition {
  return {
    key,
    label: fieldLabels[key] ?? key,
    technical: true,
    visibleInTable: false,
  };
}

const businessFields = (keys: string[]) => keys.map(businessField);
const technicalFields = (keys: string[]) => keys.map(technicalField);

