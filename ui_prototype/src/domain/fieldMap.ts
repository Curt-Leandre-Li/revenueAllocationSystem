export function getRouteFieldMapping(routePath: string) {
  return routeFieldMappings.find((mapping) => mapping.routePath === routePath);
}

export function getVisibleTableFields(mapping: RouteFieldMapping) {
  return mapping.tableFields.filter((field) => field.visibleInTable && !field.technical);
}

export function getFieldMapIntegrityReport() {
  const unlabeledBusinessFields = routeFieldMappings.flatMap((mapping) =>
    mapping.tableFields
      .filter((field) => field.label === field.key)
      .map((field) => `${mapping.routePath}:${field.key}`),
  );
  const visibleEngineeringFields = routeFieldMappings.flatMap((mapping) =>
    [...mapping.tableFields, ...mapping.technicalFields]
      .filter((field) => engineeringFieldKeys.has(field.key))
      .filter((field) => field.visibleInTable || !field.technical)
      .map((field) => `${mapping.routePath}:${field.key}`),
  );

  return {
    unlabeledBusinessFields,
    visibleEngineeringFields,
  };
}
